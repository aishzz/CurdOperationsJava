public class MathematicalOperation {
	
		public static void main(String[] args) {
			int x = 12;
			int y = 12;
			if (x != 0 && y != 0) {
				int d1 = x + y;
				int d2 = x - y;
				double d3 = x * y;
				double d4 = x / y;
				System.out.println(d1);
				System.out.println(d2);
				System.out.println(d3);
				System.out.println(d4);

			}
		}

}
